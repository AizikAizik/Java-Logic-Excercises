    private void jtfNum1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfNum1KeyTyped
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            jtfNum1.setBorder(javax.swing.BorderFactory.createLineBorder(null));
        } else {
            evt.consume();
            jtfNum1.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0), 2));
        }

        if (evt.getKeyChar() == com.sun.glass.events.KeyEvent.VK_BACKSPACE) {
            jtfNum1.setBorder(javax.swing.BorderFactory.createLineBorder(null));
        }
    }//GEN-LAST:event_jtfNum1KeyTyped

