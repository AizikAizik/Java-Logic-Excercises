    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed

        if ((jtfNum1.getText().equals("")) || (jtfNum2.getText().equals(""))) {
//           
            JOptionPane.showMessageDialog(null, "The input fields cannot be empty!", "Error Message!", JOptionPane.ERROR_MESSAGE);
            if (jtfNum1.getText().equals("")) {
                jtfNum1.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0), 2));
            }
            if ((jtfNum2.getText().equals(""))) {
                jtfNum2.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0), 2));
            }
        } else {

            int firstIn = Integer.parseInt(jtfNum1.getText());
            int secondIn = Integer.parseInt(jtfNum2.getText());
            int sum = firstIn + secondIn;
            String total = Integer.toString(sum);

            ResScreen.setText(total);
            jtfNum1.setText("");
            jtfNum2.setText("");

        }
    }//GEN-LAST:event_addBtnActionPerformed

